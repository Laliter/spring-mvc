package com.spring.model;

public class Mytest {
	//定义三个属性，分别对应数据库中的字段
	int myid;
	String mydata;
	String myname;
	//getter、setter方法
	public int getMyid() {
		return myid;
	}
	public void setMyid(int myid) {
		this.myid = myid;
	}
	public String getMydata() {
		return mydata;
	}
	public void setMydata(String mydata) {
		this.mydata = mydata;
	}
	public String getMyname() {
		return myname;
	}
	public void setMyname(String myname) {
		this.myname = myname;
	}
	
}
